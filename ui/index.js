  escapeHtml(value = "") {
    return String(value ?? "").replace(
      /[&<>"']/g,
      (char) =>
        ({
          "&": "&amp;",
          "<": "&lt;",
          ">": "&gt;",
          '"': "&quot;",
          "'": "&#039;",
        })[char],
    );
  },

  safeAttr(value = "") {
    return this.escapeHtml(value).replace(/`/g, "&#096;");
  },

  safeUrl(value = "") {
    const url = String(value ?? "").trim();
    if (!url) return "";
    // Allow only absolute http/https or local paths starting with /
    // This explicitly blocks protocol-relative URLs (//) and dangerous schemes (javascript:, data:, etc.)
    if (
      /^https?:\/\//i.test(url) ||
      (url.startsWith("/") && !url.startsWith("//")) ||
      url.startsWith("#")
    ) {
      return this.safeAttr(url);
    }
    return "";
  },

